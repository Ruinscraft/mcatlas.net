			<h1>MCATLAS Rules</h1>
			<div style="text-align: left; z-index: 100; position: relative">
				<h3>General</h3>
				<ol id="rule-list">
					<li class="rule-list-entry">Do not use client-side cheats or hacks. You may use mods that do not provide a large advantage in gameplay (Optifine, minimaps, etc.)</li>
					<li class="rule-list-entry">Do not exploit any server-side bugs. <a href="https://discord.gg/srSSSgJ">Report them!</a></li>
					<li class="rule-list-entry">Do not attempt to lag or cause harm to the server.</li>
					<li class="rule-list-entry">No inappropriate material (builds, chat, town names, etc). Includes swearing. Keep it PG.</li>
					<li class="rule-list-entry">No harassing or bullying other players.</li>
					<li class="rule-list-entry">No spamming or flooding chat.</li>
					<li class="rule-list-entry">Do not advertise other servers or products.</li>
				</ol>
				<h3>Builds</h3>
				<ol id="rule-list">
					<li class="rule-list-entry">Do not significantly alter or destroy the surface of the world. You can cut down a tree, but don't blow up a mountain.</li>
					<li class="rule-list-entry">Do not build unrealistic structures (sky islands, floating towns, etc).</li>
					<li class="rule-list-entry">Do not create large islands (more than 1x1 chunk) in oceans. You are not allowed to create multiple islands in one area.</li>
					<li class="rule-list-entry">Do not block waterways. Being able to travel by water around the map is important!</li>
				</ol>
				<h3>Griefing and Theft</h3>
				<ol id="rule-list">
					<li class="rule-list-entry">Griefing or destroying things at the edge of a town's claim is not allowed. Mining near claims is fine.</li>
					<li class="rule-list-entry">If a town/build has no claims to it and is not near a claim, you may claim/loot/destroy it (but please don't make it ugly).</li>
					<li class="rule-list-entry">Griefing canals, bridges, paths, and other transportation methods in unclaimed areas is not allowed.</li>
					<li class="rule-list-entry">Stealing is not allowed. Lock your chests to prevent this.</li>
				</ol>
				<h3>Towny (Town and Nations)</h3>
				<ol id="rule-list">
					<li class="rule-list-entry">Towns can be named anything, but we recommend naming them after real life places. If you go to Chicago, you expect to see Chicago, not HerobrineCity.</li>
					<li class="rule-list-entry">Nations must be named after a formerly or currently existing nation, territory, geographical feature, or city. You can add titles such as "Empire" to the end of them.</li>
					<li class="rule-list-entry">Towns and nation names shouldn't be so similar to another town or nation that someone could be confused between them. ("Britain" vs. "GreatBritain")</li>
					<li class="rule-list-entry">Towns can be any distance away from a nation capital. The nation's name must be relevant to the nation's capital's location.</li>
					<li class="rule-list-entry">Do not create a town next to another town just to block the other town's growth. If you're a nearby town, you are allowed to expand next to it.</li>
				</ol>
				<h3>PvP</h3>
				<ol id="rule-list">
					<li class="rule-list-entry">Spawn killing or trapping a player is not allowed.</li>
					<li class="rule-list-entry">Killing players is not allowed (unless PvP is enabled by the town). If you can't PvP, don't use lava/etc to kill someone.</li>
				</ol>
